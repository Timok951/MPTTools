W_NS = "http://schemas.openxmlformats.org/wordprocessingml/2006/main"
DRAWING_TAG = f"{{{W_NS}}}drawing"


def set_paragraph_text(paragraph, text: str) -> None:
    runs = paragraph.runs
    if runs:
        runs[0].text = text
        for run in runs[1:]:
            run.text = ""
    else:
        paragraph.add_run(text)


def set_paragraph_placeholder(paragraph, text: str) -> None:
    runs = paragraph.runs
    for run in runs:
        run.text = ""
    run = runs[0] if runs else paragraph.add_run()
    run.text = text
    run.font.color.rgb = RGBColor(0xC0, 0x00, 0x00)
    run.font.underline = True
    for extra in paragraph.runs[1:]:
        extra.text = ""


def set_cell_text(cell, text: str) -> None:
    if cell.paragraphs:
        set_paragraph_text(cell.paragraphs[0], text)
    else:
        cell.add_paragraph(text)


def remove_inline_images(doc: Document) -> None:
    for paragraph in doc.paragraphs:
        for run in list(paragraph.runs):
            if run._element.findall(f".//{DRAWING_TAG}"):
                run._element.getparent().remove(run._element)

    for table in doc.tables:
        for row in table.rows:
            for cell in row.cells:
                for paragraph in cell.paragraphs:
                    for run in list(paragraph.runs):
                        if run._element.findall(f".//{DRAWING_TAG}"):
                            run._element.getparent().remove(run._element)


