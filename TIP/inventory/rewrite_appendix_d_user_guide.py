def main() -> None:
    if not DOC.exists():
        raise SystemExit(f"File not found: {DOC}")
    doc = Document(str(DOC))
    remove_inline_images(doc)
    rewrite_body(doc)
    rewrite_tables(doc)
    doc.save(str(DOC))
    print(f"Updated: {DOC.name}")


if __name__ == "__main__":
    main()
